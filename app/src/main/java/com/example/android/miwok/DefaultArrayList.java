public class DefaultArrayList {

    private String mMiwok;
    private String mDefault;

    public DefaultArrayList(String Default,String Miwok){
        mDefault = Default;
        mMiwok = Miwok;
    }

    public String getmMiwok(){
        return mMiwok;
    }

    public String getmDefault(){
        return mDefault;
    }

}
